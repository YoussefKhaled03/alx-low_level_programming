#include "3-calc.h"

/**
 * op_add - return the sum of a & b
 * @a: first num
 * @a: second num
 *
 * RETURN: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return the sub of a & b
 * @a: first num
 * @b: second num
 *
 * RETURN: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return the mul of a & b
 * @a: first num
 * @b: second num
 *
 * RETURN: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return the div of a & b
 * @a: first num
 * @b: second num
 *
 * RETURN: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return the mod of a & b
 * @a: first num
 * @b: second num
 *
 * RETURN: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
